//script execution

//Asian grading scale
let grade = "B";

switch (grade) {
    case "A":
        console.log("Average");
        break;
    case "B":
        console.log("Bellow Average");
        break;
    case "C":
        console.log("Can't eat dinner");
        break;
    case "D":
        console.log("Don't come home");
        break;
    case "F":
        console.log("Find a new family");
        break;
    default:
        console.log("Invalid grade");
        break;
}