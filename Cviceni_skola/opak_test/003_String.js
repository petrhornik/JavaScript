//druhy spojevání stringu v JS

const myName = "John Doe";

console.log("My name is " + myName); //vypíše My name is John Doe

console.log("My name is", myName); //vypíše My name is John Doe

console.log(`My name is ${myName}`); //vypíše My name is John Doe, v {} lze spouštět i samostatné funkce/kódy